import Foundation

enum SidebarLayoutMetrics {
    static let contentHorizontalPadding: CGFloat = 12
    static let sectionHorizontalPadding: CGFloat = 12
    static let sectionSpacing: CGFloat = 10
    static let rowCornerRadius: CGFloat = 8
    static let rowVerticalPadding: CGFloat = 5
    static let rowHorizontalPadding: CGFloat = 8
    static let rowSpacing: CGFloat = 2
    static let rowContentWidth: CGFloat = 200
    static let rowOuterWidth: CGFloat = rowContentWidth + (rowHorizontalPadding * 2)
    static let scrollContainerWidth: CGFloat = rowOuterWidth
}
